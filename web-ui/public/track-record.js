// ── Tracker Feature ─────────────────────────────────────────────
const TRACKER_KEY = 'xmore_tracked_signals';

function getTracked() {
  try { return JSON.parse(localStorage.getItem(TRACKER_KEY)) || []; }
  catch { return []; }
}

function saveTracked(items) {
  localStorage.setItem(TRACKER_KEY, JSON.stringify(items));
}

function toggleTrack(btn) {
  const { signalId, symbol, action, entry, date } = btn.dataset;
  const id = signalId || `${date}-${symbol}`;
  let tracked = getTracked();
  const idx = tracked.findIndex(t => t.signalId === id);

  if (idx === -1) {
    tracked.push({ signalId: id, symbol, action, entryPrice: parseFloat(entry) || null, trackedAt: new Date().toISOString(), signalDate: date, currentPnl: null });
    btn.classList.add('tracked');
    btn.querySelector('span').textContent = '◉';
    showMicroToast(`${symbol} added to tracker`);
  } else {
    tracked.splice(idx, 1);
    btn.classList.remove('tracked');
    btn.querySelector('span').textContent = '◎';
    showMicroToast(`${symbol} removed`);
  }
  saveTracked(tracked);
  renderTrackerPanel();
  updateTrackerBadge();
}

function renderTrackerPanel() {
  const tracked = getTracked();
  const body    = document.getElementById('trackerBody');
  const pnlSum  = document.getElementById('trackerPnlSummary');
  if (!body) return;

  if (!tracked.length) {
    body.innerHTML = `<div style="padding:24px 16px;text-align:center;color:rgba(255,255,255,0.3);font-size:12px">Click ◎ on any signal to track it</div>`;
    if (pnlSum) pnlSum.textContent = '';
    return;
  }

  let totalPnl = 0, resolved = 0;
  body.innerHTML = tracked.map(tk => {
    const pnlCls  = tk.currentPnl > 0 ? 'positive' : tk.currentPnl < 0 ? 'negative' : 'pending';
    const pnlTxt  = tk.currentPnl != null ? `${tk.currentPnl > 0 ? '+' : ''}${(tk.currentPnl).toFixed(2)}%` : '—';
    if (tk.currentPnl != null) { totalPnl += tk.currentPnl; resolved++; }
    return `<div class="tracker-item">
      <div>
        <div class="tracker-symbol">${tk.symbol}</div>
        <div class="tracker-meta">${tk.action || ''} · ${tk.signalDate || ''}</div>
      </div>
      <div class="tracker-pnl ${pnlCls}">${pnlTxt}</div>
    </div>`;
  }).join('');

  if (resolved > 0 && pnlSum) {
    const avg = (totalPnl / resolved).toFixed(2);
    const c   = totalPnl >= 0 ? '#4ade80' : '#f87171';
    pnlSum.innerHTML = `Avg: <span style="color:${c};font-weight:700">${totalPnl>=0?'+':''}${avg}%</span>`;
  }
}

function updateTrackerBadge() {
  const count = getTracked().length;
  const el1   = document.getElementById('trackerCount');
  const el2   = document.getElementById('trackerCountHeader');
  if (el1) { el1.textContent = count; }
  if (el2) { el2.textContent = count; el2.dataset.count = count; }
}

function showMicroToast(msg) {
  const t2 = document.createElement('div');
  t2.style.cssText = 'position:fixed;bottom:24px;left:50%;transform:translateX(-50%);background:rgba(212,168,83,0.95);color:#0D1B3E;padding:8px 20px;border-radius:20px;font-size:12px;font-weight:700;letter-spacing:0.06em;z-index:9999;animation:fadeUp 0.3s ease';
  t2.textContent = msg;
  document.body.appendChild(t2);
  setTimeout(() => t2.remove(), 2200);
}

function initTracker() {
  const clearBtn = document.getElementById('trackerClear');
  if (clearBtn) clearBtn.addEventListener('click', () => {
    localStorage.removeItem(TRACKER_KEY);
    renderTrackerPanel();
    updateTrackerBadge();
    document.querySelectorAll('.track-btn.tracked').forEach(b => { b.classList.remove('tracked'); const s = b.querySelector('span'); if(s) s.textContent='◎'; });
  });
  renderTrackerPanel();
  updateTrackerBadge();
}

function renderPagination(pag) {
  const el = document.getElementById('logPagination');
  if (!el || !pag || pag.pages <= 1) { if(el) el.innerHTML=''; return; }

  const { page, pages, total } = pag;
  let html = '';

  html += `<button class="tr-page-btn" onclick="goPage(${page-1})" ${page<=1?'disabled':''}>‹</button>`;

  const start = Math.max(1, page - 2);
  const end   = Math.min(pages, page + 2);

  if (start > 1) html += `<button class="tr-page-btn" onclick="goPage(1)">1</button>`;
  if (start > 2) html += `<span class="tr-page-info">…</span>`;
  for (let i = start; i <= end; i++) {
    html += `<button class="tr-page-btn${i===page?' active':''}" onclick="goPage(${i})">${i}</button>`;
  }
  if (end < pages - 1) html += `<span class="tr-page-info">…</span>`;
  if (end < pages) html += `<button class="tr-page-btn" onclick="goPage(${pages})">${pages}</button>`;

  html += `<button class="tr-page-btn" onclick="goPage(${page+1})" ${page>=pages?'disabled':''}>›</button>`;
  html += `<span class="tr-page-info">${total} total</span>`;

  el.innerHTML = html;
}

function goPage(p) {
  logPage = p;
  loadLog();
  window.scrollTo({ top: document.getElementById('logTableBody').closest('.tr-section').offsetTop - 80, behavior: 'smooth' });
}

// ── Walk-Forward Backtest ───────────────────────────────────────
async function loadBacktest() {
  const container = document.getElementById('backtestContent');
  if (!container) return;
  container.innerHTML = `<div class="tr-loading">${t('loading')}</div>`;

  try {
    const data = await xfetch('/api/track-record/backtest');
    if (!data) { container.innerHTML = `<div class="tr-loading">${t('backtestEmpty')}</div>`; return; }

    // New endpoint wraps results; old returned a bare array
    const results = Array.isArray(data) ? data : (data.results || []);
    if (data.status === 'pending' || !results.length) {
      container.innerHTML = `<div class="tr-loading">${t('backtestEmpty')}</div>`;
      return;
    }

    // Sort by directional_accuracy desc
    const sorted = [...results].sort((a, b) => Number(b.directional_accuracy) - Number(a.directional_accuracy));

    // Aggregate stats for summary strip
    const avgAcc  = sorted.reduce((s, r) => s + Number(r.accuracy || 0), 0) / sorted.length;
    const avgDir  = sorted.reduce((s, r) => s + Number(r.directional_accuracy || 0), 0) / sorted.length;
    const avgPnl  = sorted.reduce((s, r) => s + Number(r.signal_pnl_pct || 0), 0) / sorted.length;
    const runDate = sorted[0]?.run_date;

    container.innerHTML = `
      <div class="tr-bt-summary">
        <div class="tr-bt-stat">
          <div class="tr-bt-stat-label">${_LANG === 'ar' ? 'الأسهم المُختبرة' : 'Stocks Tested'}</div>
          <div class="tr-bt-stat-val">${sorted.length}</div>
        </div>
        <div class="tr-bt-stat">
          <div class="tr-bt-stat-label">${_LANG === 'ar' ? 'متوسط الدقة' : 'Avg Accuracy'}</div>
          <div class="tr-bt-stat-val ${avgAcc >= 55 ? 'pos' : avgAcc >= 45 ? '' : 'neg'}">${fmt(avgAcc)}%</div>
        </div>
        <div class="tr-bt-stat">
          <div class="tr-bt-stat-label">${_LANG === 'ar' ? 'متوسط الدقة الاتجاهية' : 'Avg Directional Acc.'}</div>
          <div class="tr-bt-stat-val ${avgDir >= 55 ? 'pos' : avgDir >= 45 ? '' : 'neg'}">${fmt(avgDir)}%</div>
        </div>
        <div class="tr-bt-stat">
          <div class="tr-bt-stat-label">${_LANG === 'ar' ? 'متوسط ربح الإشارة' : 'Avg Signal P&L'}</div>
          <div class="tr-bt-stat-val ${avgPnl > 0 ? 'pos' : avgPnl < 0 ? 'neg' : ''}">${fmtPct(avgPnl, 2)}</div>
        </div>
        <div class="tr-bt-stat">
          <div class="tr-bt-stat-label">${_LANG === 'ar' ? 'آخر تشغيل' : 'Last Run'}</div>
          <div class="tr-bt-stat-val" style="font-size:13px">${fmtDate(runDate)}</div>
        </div>
      </div>
      <div class="tr-table-wrap">
        <table class="tr-table tr-table--backtest tr-table--cards">
          <thead>
            <tr>
              <th>${t('colSym')}</th>
              <th>${t('colAccuracy')}</th>
              <th>${t('colDirAcc')}</th>
              <th>${t('colSignalPnl')}</th>
              <th>${t('colLastRun')}</th>
            </tr>
          </thead>
          <tbody>
            ${sorted.map(row => {
              const acc  = Number(row.accuracy);
              const dir  = Number(row.directional_accuracy);
              const pnl  = Number(row.signal_pnl_pct);
              const labels = {
                sym: t('colSym'),
                acc: t('colAccuracy'),
                dir: t('colDirAcc'),
                pnl: t('colSignalPnl'),
                run: t('colLastRun')
              };
              return `<tr>
                <td data-label="${labels.sym}" style="font-weight:700">${row.symbol}</td>
                <td data-label="${labels.acc}" class="${acc >= 55 ? 'tr-alpha-pos' : acc < 45 ? 'tr-alpha-neg' : ''}">${fmt(acc)}%</td>
                <td data-label="${labels.dir}">
                  <div class="tr-win-bar">
                    <div class="tr-win-bar-track"><div class="tr-win-bar-fill" style="width:${Math.min(dir, 100)}%"></div></div>
                    <span class="${dir >= 55 ? 'tr-alpha-pos' : dir < 45 ? 'tr-alpha-neg' : ''}">${fmt(dir)}%</span>
                  </div>
                </td>
                <td data-label="${labels.pnl}" class="${pnl > 0 ? 'tr-alpha-pos' : pnl < 0 ? 'tr-alpha-neg' : ''}">${fmtPct(pnl, 2)}</td>
                <td data-label="${labels.run}" style="color:#555;font-size:11px">${fmtDate(row.run_date)}</td>
              </tr>`;
            }).join('')}
          </tbody>
        </table>
      </div>
    `;
  } catch (e) {
    container.innerHTML = `<div class="tr-loading">—</div>`;
    console.error('Backtest error:', e);
  }
}

